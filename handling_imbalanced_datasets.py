import  numpy as np
import pandas as pd

data=pd.read_csv('/content/credit_data.csv')

data.head()

"""# **Finding Distribution**"""

data['Class'].value_counts()

"""# 0.0 --> 13897

# 1.0  -->     56  
# See the difference in Occurance, Highly Imbalanced

# **Separating labels**
"""

legit = data[data.Class==0]
fraud= data[data.Class==1]

print(legit.shape)
print(fraud.shape)

"""# **Undersampling**"""

legit_sample=legit.sample(n=56)

print(legit_sample.shape)

"""# **Concatinate**"""

balanced_data= pd.concat([legit_sample,fraud],axis=0)

balanced_data.head()

balanced_data.tail()

